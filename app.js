  function startQuiz(questions) {
    let currentQuestion = 0;
    let score = 0;
    let timerSeconds = 10;
  
    const questionElement = document.getElementById("question");
    const choicesElement = document.getElementById("choices");
    const scoreElement = document.getElementById("score");
    const timerElement = document.getElementById("timer");
  
    let timerInterval;

    function showQuestion() {
      clearInterval(timerInterval);
      const question = questions[currentQuestion];
      questionElement.innerText = question.question;
      choicesElement.innerHTML = "";
      const choicesContainer = document.createElement("div");
      choicesContainer.className = "choices-container";
      for (let i = 0; i < question.choices.length; i++) {
        const choice = question.choices[i];
        const button = document.createElement("button");
        button.innerText = choice;
        button.addEventListener("click", function() {
          checkAnswer(choice);
        });
        choicesContainer.appendChild(button);
      }
      choicesElement.appendChild(choicesContainer);
      timerSeconds = 10;
      timerElement.innerText = `Time remaining: ${timerSeconds} seconds`;
      timerInterval = setInterval(() => {
        timerSeconds--;
        if (timerSeconds > 0) {
          timerElement.innerText = `Time remaining: ${timerSeconds} seconds`;
        } else {
          clearInterval(timerInterval);
          checkAnswer(null);
        }
      }, 1000);
      document.getElementById("easy-btn").style.display = "none";
      document.getElementById("medium-btn").style.display = "none";
      document.getElementById("hard-btn").style.display = "none";
    }
  
    function checkAnswer(choice) {
      const question = questions[currentQuestion];
      if (choice === question.answer) {
        score++;
      }
      currentQuestion++;
      if (currentQuestion < questions.length) {
        showQuestion();
      } else {
        endQuiz();
      }
    }
  
    function endQuiz() {
      questionElement.innerText = "Quiz complete!";
      choicesElement.innerHTML = "";
      scoreElement.innerText = `You scored ${score} out of ${questions.length}!`;
      timerElement.style.display = "none";
      document.getElementById("play-again").style.display = "block";
    }
  
    function resetQuiz() {
      currentQuestion = 0;
      score = 0;
      timerSeconds = 10;
      clearInterval(timerInterval);
      questionElement.innerText = "";
      choicesElement.innerHTML = "";
      scoreElement.innerText = "";
      timerElement.innerHTML = "";
      timerElement.style.display = "block";
      document.getElementById("play-again").style.display = "none";
      document.getElementById("easy-btn").style.display = "block";
      document.getElementById("medium-btn").style.display = "block";
      document.getElementById("hard-btn").style.display = "block";
    }
  
    showQuestion();
  
    document.getElementById("play-again").addEventListener("click", function() {
      resetQuiz();
    });
  }    